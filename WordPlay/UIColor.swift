import UIKit
extension UIColor {
    class func lightRedColor() -> UIColor {
        return UIColor(red: 240.0/255.0, green: 140.0/255.0, blue: 120.0/255.0, alpha: 1.0)
    }
    class func lightBlueColor() -> UIColor {
        return UIColor(red: 144.0/255.0, green: 195.0/255.0, blue: 212.0/255.0, alpha: 1.0)
    }
    class func lightGreenColor() -> UIColor {
        return UIColor(red: 161.0/255.0, green: 212.0/255.0, blue: 144.0/255.0, alpha: 1.0)
    }
    class func darkRedColor() -> UIColor {
        return UIColor(red: 201.0/255.0, green: 80.0/255.0, blue: 40.0/255.0, alpha: 1.0)
    }
    class func darkBlueColor() -> UIColor {
        return UIColor(red: 39.0/255.0, green: 148.0/255.0, blue: 184.0/255.0, alpha: 1.0)
    }
    class func darkGreenColor() -> UIColor {
        return UIColor(red: 84.0/255.0, green: 163.0/255.0, blue: 57.0/255.0, alpha: 1.0)
    }


}